package org.lazydog.repository.criterion;


/**
 * Comparison operator.
 * 
 * @author  Ron Rickard
 */
public enum ComparisonOperator {
    EQUAL,
    GREATER_THAN,
    GREATER_THAN_OR_EQUAL,
    IS_EMPTY,
    IS_NOT_EMPTY,
    IS_NULL,
    IS_NOT_NULL,
    LESS_THAN,
    LESS_THAN_OR_EQUAL,
    LIKE,
    MEMBER_OF,
    NOT_EQUAL,
    NOT_LIKE,
    NOT_MEMBER_OF;
};
